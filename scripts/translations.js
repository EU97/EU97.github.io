// Get current language from localStorage or default to English
function getCurrentLanguage() {
  return localStorage.getItem('preferredLanguage') || 'en';
}

// Set current language
function setCurrentLanguage(lang) {
  localStorage.setItem('preferredLanguage', lang);
}

// Apply translations to the page
function applyTranslations(lang) {
  const t = translations[lang];
  if (!t) return;
  
  // Update HTML lang attribute
  document.documentElement.lang = lang;
  
  // Navigation
  const navLinks = document.querySelectorAll('.nav-link');
  if (navLinks[0]) navLinks[0].textContent = t.nav.home;
  if (navLinks[1]) navLinks[1].textContent = t.nav.about;
  if (navLinks[2]) navLinks[2].textContent = t.nav.timeline;
  if (navLinks[3]) navLinks[3].textContent = t.nav.projects;
  if (navLinks[4]) navLinks[4].textContent = t.nav.tools;
  if (navLinks[5]) navLinks[5].textContent = t.nav.contact;
  
  // Hero Section
  const heroBadge = document.querySelector('.hero-badge');
  if (heroBadge) {
    const svg = heroBadge.querySelector('svg');
    heroBadge.innerHTML = '';
    if (svg) heroBadge.appendChild(svg);
    heroBadge.appendChild(document.createTextNode(t.hero.badge));
  }
  
  const heroTitle = document.querySelector('.hero-title .title-gradient');
  if (heroTitle) heroTitle.textContent = t.hero.title2;
  
  const heroSubtitle = document.querySelector('.hero-subtitle');
  if (heroSubtitle) heroSubtitle.textContent = t.hero.subtitle;
  
  const heroDescription = document.querySelector('.hero-description');
  if (heroDescription) heroDescription.textContent = t.hero.description;
  
  const exploreCta = document.querySelector('.hero-cta .btn-primary span');
  if (exploreCta) exploreCta.textContent = t.hero.exploreCta;
  
  const contactCta = document.querySelector('.hero-cta .btn-secondary span');
  if (contactCta) contactCta.textContent = t.hero.contactCta;
  
  const statLabels = document.querySelectorAll('.stat-label');
  if (statLabels[0]) statLabels[0].textContent = t.hero.stat1Label;
  if (statLabels[1]) statLabels[1].textContent = t.hero.stat2Label;
  if (statLabels[2]) statLabels[2].textContent = t.hero.stat3Label;
  if (statLabels[3]) statLabels[3].textContent = t.hero.stat4Label;
  
  // About Section
  const aboutBadge = document.querySelector('.about .section-badge');
  if (aboutBadge) aboutBadge.textContent = t.about.badge;
  
  const aboutTitle = document.querySelector('.about .section-header h2');
  if (aboutTitle) aboutTitle.textContent = t.about.title;
  
  const aboutGreeting = document.querySelector('.about-intro h3');
  if (aboutGreeting) aboutGreeting.textContent = t.about.greeting;
  
  const aboutRole = document.querySelector('.about-role');
  if (aboutRole) aboutRole.textContent = t.about.role;
  
  const bioParagraphs = document.querySelectorAll('.about-bio p');
  if (bioParagraphs[0]) bioParagraphs[0].textContent = t.about.bio1;
  if (bioParagraphs[1]) bioParagraphs[1].textContent = t.about.bio2;
  if (bioParagraphs[2]) bioParagraphs[2].textContent = t.about.bio3;
  
  const aboutBtns = document.querySelectorAll('.about-text .btn');
  if (aboutBtns[0]) aboutBtns[0].textContent = t.about.exploreBtn;
  if (aboutBtns[1]) aboutBtns[1].textContent = t.about.githubBtn;
  
  // Projects Section
  const projectsBadge = document.querySelector('.projects .section-badge');
  if (projectsBadge) projectsBadge.textContent = t.projects.badge;
  
  const projectsTitle = document.querySelector('.projects .section-header h2');
  if (projectsTitle) projectsTitle.textContent = t.projects.title;
  
  const projectsSubtitle = document.querySelector('.projects .section-header p');
  if (projectsSubtitle) projectsSubtitle.textContent = t.projects.subtitle;
  
  // Project Tabs
  const projectTabSpans = document.querySelectorAll('.tab-btn span:last-child');
  if (projectTabSpans[0]) projectTabSpans[0].textContent = t.projects.allTab;
  if (projectTabSpans[1]) projectTabSpans[1].textContent = t.projects.industrialTab;
  if (projectTabSpans[2]) projectTabSpans[2].textContent = t.projects.researchTab;
  if (projectTabSpans[3]) projectTabSpans[3].textContent = t.projects.controlTab;
  if (projectTabSpans[4]) projectTabSpans[4].textContent = t.projects.simulationTab;
  if (projectTabSpans[5]) projectTabSpans[5].textContent = t.projects.academicTab;
  
  // Skills Section
  const skillsBadge = document.querySelector('.skills .section-badge');
  if (skillsBadge) skillsBadge.textContent = t.skills.badge;
  
  const skillsTitle = document.querySelector('.skills .section-header h2');
  if (skillsTitle) skillsTitle.textContent = t.skills.title;
  
  const skillsSubtitle = document.querySelector('.skills .section-header p');
  if (skillsSubtitle) skillsSubtitle.textContent = t.skills.subtitle;
  
  const categoryTitles = document.querySelectorAll('.category-title h3');
  if (categoryTitles[0]) categoryTitles[0].textContent = t.skills.controlTitle;
  if (categoryTitles[1]) categoryTitles[1].textContent = t.skills.industrialTitle;
  if (categoryTitles[2]) categoryTitles[2].textContent = t.skills.softwareTitle;
  if (categoryTitles[3]) categoryTitles[3].textContent = t.skills.simulationTitle;
  if (categoryTitles[4]) categoryTitles[4].textContent = t.skills.qualityTitle;
  if (categoryTitles[5]) categoryTitles[5].textContent = t.skills.learningTitle;
  
  // Contact Section
  const contactBadge = document.querySelector('.contact .section-badge');
  if (contactBadge) contactBadge.textContent = t.contact.badge;
  
  const contactTitle = document.querySelector('.contact .section-header h2');
  if (contactTitle) contactTitle.textContent = t.contact.title;
  
  const contactIntro = document.querySelector('.contact-intro');
  if (contactIntro) contactIntro.textContent = t.contact.intro;
  
  const contactItems = document.querySelectorAll('.contact-item');
  if (contactItems[0]) {
    contactItems[0].querySelector('h4').textContent = t.contact.collabTitle;
    contactItems[0].querySelector('p').textContent = t.contact.collabDesc;
  }
  if (contactItems[1]) {
    contactItems[1].querySelector('h4').textContent = t.contact.researchTitle;
    contactItems[1].querySelector('p').textContent = t.contact.researchDesc;
  }
  if (contactItems[2]) {
    contactItems[2].querySelector('h4').textContent = t.contact.academicTitle;
    contactItems[2].querySelector('p').textContent = t.contact.academicDesc;
  }
  
  const connectTitle = document.querySelector('.social-section h3');
  if (connectTitle) connectTitle.textContent = t.contact.connectTitle;
  
  // Footer
  const footerP = document.querySelector('.footer p');
  if (footerP) footerP.textContent = t.footer.copyright;
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
  const currentLang = getCurrentLanguage();
  applyTranslations(currentLang);
  
  // Update language toggle button if it exists
  const langToggle = document.getElementById('lang-toggle');
  if (langToggle) {
    langToggle.textContent = currentLang === 'en' ? '🇪🇸 ES' : '🇬🇧 EN';
  }
});
